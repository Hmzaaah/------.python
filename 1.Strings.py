name ="Hello, Hamza"
print(name.lower())# Convert to lowercase
print(name.upper())# Convert to uppercase
print(name.title())# Convert to title case
print(name.strip())# Remove whitespace from both ends
print(name.replace("H","J"))# Replace a substring with another substring
print(name.split(","))# Split the string into a list
print(name.find("Hamza"))# Find the index of a substring
print(name.startswith("Hello"))# Check if the string starts with a substring
print(name.endswith("Hamza"))# Check if the string ends with a substring
print(name.count("a"))# Count occurrences of a substring
print(name.index("H"))# Find the index of a substring (raises an error if not found)
print(name.isalpha())# Check if all characters are alphabetic
print(name.isdigit())# Check if all characters are digits
print(name.isalnum())# Check if all characters are alphanumeric
print(name.capitalize())# Capitalize the first character
print(name.center(30))# Center the string within a specified width
print(name.encode())# Encode the string to bytes
print(name.endswith("a"))# Check if the string ends with a substring
print(name.expandtabs())# Replace tabs with spaces
print(name.format())# Format the string (useful with placeholders)
print(name.format_map({}))# Format the string using a mapping
