#العمليات الحسابية في بايثون
from math import *
my_Number=5
print (3*(4+5))#تستخدم القواس لتحديد اولويات العمليات الحسابية
print (10/3)#القسمة العادية
print (10//3)#القسمة الصحيحة
print (10%3)#الباقي
print (2**3)#الاس
print (round(2.675,2))#التقريب
print (abs(-7.5))#القيمة المطلقة
print(str(my_Number))#تحويل الرقم الى نص
print(float(my_Number))#تحويل الرقم الى عشري
print(complex(my_Number))#تحويل الرقم الى مركب
print(bin(my_Number))#تحويل الرقم الى ثنائي
print(oct(my_Number))#تحويل الرقم الى ثماني